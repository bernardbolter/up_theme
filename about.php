			<p>Bernard Bolter started the Pullup Gallery in his hometown of San Francisco around the end of 2013 as a street based studio and gallery.  The gallery's purpose is to showcase studio techniques in public while at the same time developing an online platform to display the process and finished works.  Bernard has developed a special 'Easel-Wall' for this project, which is a field painting easel, where one of the legs has been replaced with a 3 foot square wall used for the display of the artwork.  The Easel-Wall makes it easy to demonstrate the Pullup Gallery's urban outdoor painting technique while engaged and inspired by the city, while at the same time uniquely displaying finished works for the public to view.  When the painting is down, the Easel-Wall compresses down enough to be pulled onto a local bus or train.  The Pullup Gallery is mobile and intends to be on the move to a city near you in the near future.</p>
		
